CfamilyHandler::CfamilyHandler()
{
   barrier = "/* --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+-- */";
}

// --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--

void CfamilyHandler::addTop( std::string & xmlFilename,
                             char        * timeBuf,
                             xmlChar     * version )
{
   outStream << "/*" << endl;
   outStream << " * This file was generated by the program errorParser" << endl;
   outStream << " * using the input file " << xmlFilename << "." << endl;
   outStream << " * Date: " << timeBuf << "." << endl;
   outStream << " *" << endl;
   if ( version != NULL ) {
      outStream << " * The version of the XML error file is " << version << "." << endl;
      outStream << " *" << endl;
   }
}

// --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--

void CfamilyHandler::addCopyright( xmlNode * node )
{
   string years, authors;
   string tmp, prefix = " * ";
   
   node = node->children;
   
   /* Go to the first element */
   while ( node->type != XML_ELEMENT_NODE ) { node = node->next; }
   stripText( node->children->content, years );
   
   /* Go to the next element */
   do { node = node->next; } while ( node->type != XML_ELEMENT_NODE );
   stripText( node->children->content, authors );
   
   outStream << " * Copyright (C) " << years << " " << authors << endl;
   
   node = node->next;
   while ( node != NULL ) {
      if ( node->type == XML_ELEMENT_NODE ) {
         outStream << " *" << endl;

         stripText( node->children->content, tmp );
         prettify( outStream, tmp, prefix, ERRP_LINE_LENGTH );
      }
      node = node->next;
   }

   outStream << " *" << endl;

}

// --+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--+--

void CfamilyHandler::addEndTop()
{
   outStream << " */" << endl << endl;
   outStream << barrier << endl << endl;
}

